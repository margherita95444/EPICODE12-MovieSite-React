export class Movie {
    constructor(
        actors, 
        directors, 
        escritors, 
        otherTitles, 
        id, 
        image,
        title, 
        rating, 
        year,
        genres
    )
    {
      this.actors = actors;
      this.directors = directors;
      this.escritors = escritors;
      this.otherTitles = otherTitles;
      this.id = id;
      this.image = image;
      this.title = title;
      this.rating = rating;
      this.year = year;
      this.genres = genres;
    }
  }
  